package com.example.chatapp.models;

public class ContactToJson{
    public String id ;
    public String name ;
    public String server ;
    public String last;
    public String lastdate ;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getServer() {
        return server;
    }

    public String getLast() {
        return last;
    }

    public String getLastdate() {
        return lastdate;
    }
}
